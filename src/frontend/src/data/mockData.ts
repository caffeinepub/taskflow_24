interface TrendEntry {
  date: string;
  attendance: number | null;
  absent: number | null;
}

export function generate30DayTrend(): TrendEntry[] {
  const data: TrendEntry[] = [];
  const base = new Date(2026, 1, 23);
  for (let i = 0; i < 30; i++) {
    const d = new Date(base);
    d.setDate(base.getDate() + i);
    const day = d.getDay();
    const isWeekend = day === 0 || day === 6;
    const rate = isWeekend ? 0 : 72 + Math.floor(Math.random() * 20);
    data.push({
      date: `${d.getMonth() + 1}/${d.getDate()}`,
      attendance: isWeekend ? null : rate,
      absent: isWeekend ? null : 100 - rate,
    });
  }
  return data;
}

